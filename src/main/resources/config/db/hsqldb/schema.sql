SET DATABASE SQL SYNTAX ORA TRUE;

CREATE TABLE IF NOT EXISTS PUBLIC.SAMPLE (
   key           INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY    NOT NULL
  ,index         INTEGER
  ,guid          VARCHAR(36)
  ,balance       NUMERIC(6,2)
  ,picture       VARCHAR(1024)
  ,age           INTEGER
  ,eye_color     VARCHAR(5)
  ,name          VARCHAR(512)
  ,gender        VARCHAR(6)
  ,company       VARCHAR(256)
  ,email         VARCHAR(256)
  ,phone         VARCHAR(64)
  ,address       VARCHAR(512)
  ,about         VARCHAR(1024)
  ,registered    DATE
  ,latitude      NUMERIC(8,6)
  ,longitude     NUMERIC(9,6)
  ,greeting      VARCHAR(1024)
  ,favorite_fruit VARCHAR(64)
  ,dept_cd       VARCHAR(18)
  ,file_id     	 VARCHAR(36)
  ,is_active     VARCHAR(1)
);
CREATE UNIQUE INDEX IF NOT EXISTS PUBLIC.IDX_SAMPLE ON PUBLIC.SAMPLE (key);

CREATE TABLE IF NOT EXISTS PUBLIC.SAMPLE_DEPT (
   dept_cd      VARCHAR(16) NOT NULL
  ,p_dept_cd	VARCHAR(16) NOT NULL
  ,dept_nm		VARCHAR(16) NOT NULL
  ,dept_lv		INTEGER
);
CREATE UNIQUE INDEX IF NOT EXISTS PUBLIC.IDX_SAMPLE_DEPT ON PUBLIC.SAMPLE_DEPT (DEPT_CD);

CREATE TABLE IF NOT EXISTS PUBLIC.COMMON_CODE (
   CODE         VARCHAR(16) NOT NULL
  ,KEY          VARCHAR(16) NOT NULL
  ,VAL          VARCHAR(16) NOT NULL
  ,REF_CODE     VARCHAR(16)
);
CREATE UNIQUE INDEX IF NOT EXISTS PUBLIC.IDX_COMMON_CODE ON PUBLIC.COMMON_CODE (CODE, KEY);
COMMENT ON TABLE PUBLIC.COMMON_CODE IS '공통코드';
COMMENT ON COLUMN PUBLIC.COMMON_CODE.CODE IS '코드'
COMMENT ON COLUMN PUBLIC.COMMON_CODE.KEY IS '키'
COMMENT ON COLUMN PUBLIC.COMMON_CODE.VAL IS '값'
COMMENT ON COLUMN PUBLIC.COMMON_CODE.REF_CODE IS '참조코드'

CREATE TABLE IF NOT EXISTS PUBLIC.COMMON_FILE (
   FILE_ID          VARCHAR(36)     NOT NULL
  ,FILE_SEQ         INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY NOT NULL
  ,FILE_NAME        VARCHAR(64)     NOT NULL
  ,FILE_NAME_EXT    VARCHAR(16)
  ,FILE_SIZE        INTEGER         NOT NULL
  ,STRD_FILE_PATH   VARCHAR(256)    NOT NULL
  ,STRD_FILE_NAME   VARCHAR(64)     NOT NULL
);
CREATE UNIQUE INDEX IF NOT EXISTS PUBLIC.IDX_COMMON_FILE ON PUBLIC.COMMON_FILE (FILE_ID, FILE_SEQ);
COMMENT ON TABLE PUBLIC.COMMON_FILE IS '파일';
COMMENT ON COLUMN PUBLIC.COMMON_FILE.FILE_ID IS '파일ID'
COMMENT ON COLUMN PUBLIC.COMMON_FILE.FILE_SEQ IS '파일시퀀스'
COMMENT ON COLUMN PUBLIC.COMMON_FILE.FILE_NAME IS '파일명'
COMMENT ON COLUMN PUBLIC.COMMON_FILE.FILE_NAME_EXT IS '파일명확장자'
COMMENT ON COLUMN PUBLIC.COMMON_FILE.FILE_SIZE IS '파일크기'
COMMENT ON COLUMN PUBLIC.COMMON_FILE.STRD_FILE_PATH IS '저장파일경로'
COMMENT ON COLUMN PUBLIC.COMMON_FILE.STRD_FILE_NAME IS '저장파일명'

CREATE TABLE IF NOT EXISTS PUBLIC.PROGRAM (
   PRGR_ID      VARCHAR(12) NOT NULL
  ,PRGR_NM      VARCHAR(64) NOT NULL
  ,PRGR_PATH    VARCHAR(16) NOT NULL
  ,PRGR_TYPE_CD VARCHAR(16) NOT NULL
  ,L_CTGR       VARCHAR(16)
  ,M_CTGR       VARCHAR(16)
  ,S_CTGR       VARCHAR(16)
);
CREATE UNIQUE INDEX IF NOT EXISTS PUBLIC.IDX_PROGRAM ON PUBLIC.PROGRAM (PRGR_ID);
COMMENT ON TABLE PUBLIC.PROGRAM IS '프로그램';
COMMENT ON COLUMN PUBLIC.PROGRAM.PRGR_ID IS '프로그램ID'
COMMENT ON COLUMN PUBLIC.PROGRAM.PRGR_NM IS '프로그램명'
COMMENT ON COLUMN PUBLIC.PROGRAM.PRGR_PATH IS '프로그램경로'
COMMENT ON COLUMN PUBLIC.PROGRAM.PRGR_TYPE_CD IS '프로그램유형코드(prgrType)';
COMMENT ON COLUMN PUBLIC.PROGRAM.L_CTGR IS '대분류'
COMMENT ON COLUMN PUBLIC.PROGRAM.M_CTGR IS '중분류'
COMMENT ON COLUMN PUBLIC.PROGRAM.S_CTGR IS '소분류'